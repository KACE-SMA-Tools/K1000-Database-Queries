SELECT NODE_SNMP_SYSTEM.ID, NODE.IP,
REPLACE(SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.11.1.1.9.1.1",FULL_WALK)+36, 3), "<", "") as "Black" ,
REPLACE(SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.11.1.1.9.1.2",FULL_WALK)+36, 3), "<", "") as "Cyan  ",
REPLACE(SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.11.1.1.9.1.3",FULL_WALK)+36, 3), "<", "") as "Magenta",
REPLACE(SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.11.1.1.9.1.4",FULL_WALK)+36, 3), "<", "") as "Yellow",
SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.5.1.1.16.1", FULL_WALK)+35, 7) as "HIG Number",
SUBSTRING(FULL_WALK, LOCATE("SNMPv2-MIB::sysDescr.0", FULL_WALK)+25, 20) as Model,
CASE
    WHEN FULL_WALK REGEXP "SNMPv2-SMI::mib-2.43.18.1.1.8.1.1" THEN 
        SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.1", FULL_WALK)+37, LOCATE("<br>", FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.1", FULL_WALK)) - LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.1", FULL_WALK)-38)
END as "Warning1",

CASE
    WHEN FULL_WALK REGEXP "SNMPv2-SMI::mib-2.43.18.1.1.8.1.2" THEN 
        SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.2", FULL_WALK)+37, LOCATE("<br>", FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.2", FULL_WALK)) - LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.2", FULL_WALK)-38)
END as "Warning2",

CASE
    WHEN FULL_WALK REGEXP "SNMPv2-SMI::mib-2.43.18.1.1.8.1.3" THEN 
        SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.3", FULL_WALK)+37, LOCATE("<br>", FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.3", FULL_WALK)) - LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.3", FULL_WALK)-38)
END as "Warning3",

CASE
    WHEN FULL_WALK REGEXP "SNMPv2-SMI::mib-2.43.18.1.1.8.1.4" THEN 
        SUBSTRING(FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.4", FULL_WALK)+37, LOCATE("<br>", FULL_WALK, LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.4", FULL_WALK)) - LOCATE("SNMPv2-SMI::mib-2.43.18.1.1.8.1.4", FULL_WALK)-38)
END as "Warning4"

FROM ORG1.NODE_SNMP_SYSTEM

JOIN NODE on NODE_SNMP_SYSTEM.ID = NODE.ID
WHERE SUBSTRING(FULL_WALK, LOCATE("SNMPv2-MIB::sysDescr.0", FULL_WALK)+25, 20) like "TOSHIBA%"
GROUP BY NODE_SNMP_SYSTEM.ID